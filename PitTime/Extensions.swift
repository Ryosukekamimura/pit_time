import Foundation
import SwiftUI

extension Color {
    struct MyTheme {

        // Gray Color -> Main Color
        static var beigeColor: Color {
            Color("ColorBeige")
        }

        // Navy Color -> Sub Color
        static var orangeColor: Color {
            Color("ColorOrange")
        }

        // Pink Color -> Accent Color
        static var blueColor: Color {
            Color("ColorBlue")
        }
    }
}
