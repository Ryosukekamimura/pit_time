import Foundation
import SwiftUI

extension Color {
    struct MyTheme {
        
        // Gray Color -> Main Color
        static var grayColor: Color {
            return Color("ColorGray")
        }
        
        // Navy Color -> Sub Color
        static var navyColor: Color {
            return Color("ColorNavy")
        }
        
        // Pink Color -> Accent Color
        static var pinkColor: Color {
            return Color("ColorPink")
        }
    }
}
